package variable_ex;

public class Ex01 {

	public static void main(String[] args) {
//		assign variable
//		initialize variable
		
//		datatype int, float, double, char, boolean, short, .. etc
		
		int number = 10;
		System.out.println(number);
		
		number = 5;
		System.out.println(number);
		
		
		int number2 = 100;
		int sum = number + number2;
		System.out.println(sum);
		
//		float 실수 
//		have to put 'f' in ending, and automatically rounds off in 6~7th decimal points
//		hence it is not encouraged, use double instead
		float fNumber = 1.5555555555555555555555f;
		System.out.println(fNumber);
		
//		double
		double dNumber = 1.5555555555555555555555;
		System.out.println(dNumber );
		
//		char 
//		must use ' ' not " "
		char cTest = 'c';
		System.out.println(cTest);
		
//		boolean 
		boolean isReal = true;
		boolean isFalse = false;
		System.out.println(isReal);
		System.out.println("False ! " + isFalse);
		
//		bFlag, dNumber, isReal ... is_real 하지않고 이렇게 하는걸 camel case 낙타등같다 하고 해서
//		_ 보다 대문자 사용하는게 자바에서 일반적인 방식
//		_ 는 snake tail 라고 c,파이썬 프로그래머가 일반적으로 사용함!
		
//		
//		이름: 신보람 나이: 25
//
//		3 + 5 = 8
//		3 - 5 = -2
//
//		7 X 1 = 7
//		7 X 2 = 14
//		7 X 3 = 21
		
		
		
		
		
	}

}
